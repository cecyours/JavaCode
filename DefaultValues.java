class DefaultValues{
	public static void main(String[] args) {
		
		Data mycode = new Data(); 
		
		System.out.println("DefaultValues of      int : "+mycode.i);
		System.out.println("DefaultValues of    float : "+mycode.f);
		System.out.println("DefaultValues of Character: "+mycode.ch);

	}
}
class Data{
	     int i;
		float f;
		char ch;
		
}